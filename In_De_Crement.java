import java.lang.*;

public class In_De_Crement
{
    public static void main(String[] arg)
    {
        int a = 10;

        System.out.println(a);
        System.out.println(++a);
        a++;
        System.out.println(a);
        System.out.println((--a));
        a--;
        System.out.println(a);
    }
}
